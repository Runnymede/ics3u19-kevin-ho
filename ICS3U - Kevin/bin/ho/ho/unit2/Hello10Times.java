/**Hello10Times.java
 * This program says Hello ten times.
 * @author Kevin Ho
 *November 11, 2019
 */

public class Hello10Times {

	/**
	 * Beginning of the program.
	 * @param args
	 */

	public static void main(String[] args) {

		for (int counter = 0; counter < 10; counter++) //loops 10 times
		{
			System.out.println("Hello."); //prints "Hello"
		}

	}

}
